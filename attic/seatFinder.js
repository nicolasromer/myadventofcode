const planeLength = [...Array(128).keys()];
const planeWidth = [...Array(8).keys()];
const IdMultiplier = 8;
const getSeatFromCode = code => {
    const instructions = code.split('');
    const frontBackInstructions = instructions.slice(0, 7);
    const rightLeftInstructions = instructions.slice(7);
    const row = frontBackInstructions.reduce(getSeatForInstruction, planeLength)[0];
    const col = rightLeftInstructions.reduce(getSeatForInstruction, planeWidth)[0];
    const id = row * IdMultiplier + col;
    if (typeof (id) !== 'number') {
        throw new Error('FAIL! ', code);
    }
    return { row, col, id };
};
const getSeatForInstruction = (set, instruction) => {
    const midpoint = set.length / 2;
    const keepLowerSet = instruction === front || instruction === left;
    const result = keepLowerSet
        ? set.slice(0, midpoint)
        : set.slice(midpoint);
    return result;
};
