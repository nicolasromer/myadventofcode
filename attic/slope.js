const tree = '#';
const space = '.';

const convertRaw = (data) => data.split('\n').map(row => row.trim());

const getColumn = (columnNumber, row) => row[(columnNumber-1) % row.length];

const mapRoute = (skiHillData, speedAcross = 3, speedDown = 1) => {
	const skiHill = convertRaw(skiHillData);
	
	let route = [];

	for (
		let state = {i: 1, row:1, column:1};
		state.row <= skiHill.length;
		state = {
			i: state.i++,
			row: state.row + speedDown,
			column: state.column + speedAcross
		}
	) {
	
		const whatsThere = getColumn(state.column, skiHill[state.row-1]);
		route.push(whatsThere);
	}
	return route;
}

const countTreesInRoute = route => route.filter(thing => thing === tree).length;

const routeMaps = (runs) => runs.map(r => mapRoute(raw, r[0], r[1]));

const routeTreeCounts = (maps) => maps.map(m => countTreesInRoute(m));

/*
 * where the magic happens
 */

 const finalRuns = [
	[1,1],
	[3,1],
	[5,1],
	[7,1],
	[1,2]
];

document.body.innerHTML = `<h1>
	${(()=>{
		const maps = routeMaps(finalRuns);
		const counts = routeTreeCounts(maps);
		const total = counts.reduce((acc, curr) => acc * curr);
		return total;
	})()}
</h1>`



